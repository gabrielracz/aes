void printCharBinary(unsigned char num){
	int bits = sizeof(num) * 8;
	short bit;
	for(int i = 0; i < bits; i++){
		bit = (num & (1 << (bits-i-1))) >> (bits-i-1);
		printf("%d", bit);
	}	
}

void printIntArrayBinary(int* nums, int len){
	int bits = 32;
	short bit;
	uint32_t num;
	for(int j = 0; j < len; j++){
		num = nums[j];
		for(int i = 0; i < bits; i++){
			bit = (num & (1 << bits-i-1)) >> bits-i-1;
			printf("%d", bit);
		}
		printf(",");
	}
	printf("\n");
}

void printState(uint8_t* state) {
	for(int y = 0; y < 4; y++) {
		for(int x = 0; x < 4; x++) {
			printf("%02x ", state[IX2(x, y)]);
		}
		printf("\n");
	}
	printf("\n");
}

