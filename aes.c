int pack_key(const unsigned char* key, size_t keylen, uint32_t* packed) {
	uint32_t elem = 0;
	uint32_t cword = 0;
	uint32_t cycle = 0;
	for(size_t i = 0; i < keylen; i++){
		cycle = (i+1) % 4;
		switch(cycle) {
			case 0:
				elem += key[i];
				packed[cword++] = elem;
				break;
			case 1:
				elem = key[i] << 24;
				break;
			case 2:
				elem += key[i] << 16;
				break;
			case 3:
				elem += key[i] << 8;
				break;
		}
	}
	if (cycle > 0)	//cleanup when exiting out of incomplete block
		packed[cword] = elem;

	return 0;
}

uint32_t rotword(uint32_t word, uint8_t bytes) {
	//one byte circular shift to the left
	return (word << 8) | (word >> (32 - 8));
}

uint32_t subword(uint32_t word) {
	//sbox each of the 4 bytes in word
	uint32_t s;

	s = sbox[(word & 0x000000ff)      ];
	s += sbox[(word & 0x0000ff00) >> 8 ] << 8;
	s += sbox[(word & 0x00ff0000) >> 16] << 16;
	s += sbox[(word & 0xff000000) >> 24] << 24;
	return s;
}

int key_expansion(uint32_t* key, uint32_t* round_keys) {
	for(int i = 0; i < W; i++) {
		if(i < N){
			round_keys[i] = key[i];
		}else if(i % N == 0) {
			round_keys[i] = round_keys[i-N] ^ subword(rotword(round_keys[i-1], 1)) ^ (rcon[i/N - 1] << 24);
		}else if(N > 6 && i % N == 4) {
			round_keys[i] = round_keys[i-N] ^ subword(round_keys[i-1]);
		}else {
			round_keys[i] = round_keys[i-N] ^ round_keys[i-1];
		}
	}
	return 0;
}


void unpack_round_keys(uint32_t* expanded_key, uint8_t* round_keys) {
	for(int i = 0; i < W; i++){
		int rnd = i/4;
		int col =  i % 4;

		round_keys[rnd*16 + col*4 + 0 ] = (expanded_key[i] & 0xff000000) >> 24;
		round_keys[rnd*16 + col*4 + 1 ] = (expanded_key[i] & 0x00ff0000) >> 16;
		round_keys[rnd*16 + col*4 + 2 ] = (expanded_key[i] & 0x0000ff00) >> 8;
		round_keys[rnd*16 + col*4 + 3] = (expanded_key[i] & 0x000000ff);
	}
}

//xor each byte in the state to the round key
void add_round_key(uint8_t* state, uint8_t* round_key) {
	for(int i = 0; i < 4*4; i++) {
		/*printf("%02x^%02x %02x\n", state[i], round_key[i], state[i] ^ round_key[i]);*/
		state[i] ^= round_key[i];
	}
}

void sub_bytes(uint8_t* state) {
	for(int i = 0; i < 4*4; i++) {
		state[i] = sbox[state[i]];
	}
}


//shift each row by 0, 1, 2, 3
void shift_rows(uint8_t* state) {
	//cyclic shift algorithm
	//naive temp array approach
	uint8_t tmp[4];
	for(int i  = 1; i < 4; i++) {
		tmp[0] = state[4*0 + i];
		tmp[1] = state[4*1 + i];
		tmp[2] = state[4*2 + i];
		tmp[3] = state[4*3 + i];

		/*printf("%x %x %x %x\n\n", tmp[1], tmp[1], tmp[2], tmp[3]);*/
		state[4*0 + i] = tmp[(0+i) % 4];
		state[4*1 + i] = tmp[(1+i) % 4];
		state[4*2 + i] = tmp[(2+i) % 4];
		state[4*3 + i] = tmp[(3+i) % 4];
	}
}

